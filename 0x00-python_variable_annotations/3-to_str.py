#!/usr/bin/env python3
"""
Write a type-annotated function to_str that takes a float n as argument
and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    takes a float n as argument and returns the string representation of the float
    Args
        n: float
    Return:
        the string representation of the float n.
    """
    return str(n)
