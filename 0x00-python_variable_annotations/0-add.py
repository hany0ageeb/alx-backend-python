#!/usr/bin/env python3
"""
Write a type-annotated function add that takes a float a and a float b as arguments
and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    add two floats a, b and return the result
    Args:
        a: float
        b: float
    Return
        sum of a and b
    """
    return a + b
